"""Assignment Operators are used to assign values to variables. They are a combination of an arithmetic operator and the assignment operator (=)."""

# Example of assignment operator
x = 5
print(x)  # Output: 5

# Example of addition assignment operator
x += 3  # This is equivalent to x = x + 3
print(x)  # Output: 8

# Example of subtraction assignment operator
x -= 2  # This is equivalent to x = x - 2
print(x)  # Output: 6

# Example of multiplication assignment operator
x *= 4  # This is equivalent to x = x * 4
print(x)  # Output: 24

# Example of division assignment operator
x /= 6  # This is equivalent to x = x / 6   
print(x)  # Output: 4.0

# Example of modulus assignment operator
x %= 3  # This is equivalent to x = x % 3
print(x)  # Output: 1.0

# Example of exponentiation assignment operator
x **= 2  # This is equivalent to x = x ** 2
print(x)  # Output: 1.0

# Example of floor division assignment operator
x //= 2  # This is equivalent to x = x // 2
print(x)  # Output: 0.0

