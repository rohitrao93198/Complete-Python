"""Operators are special symbols that perform specific operations on one or more operands."""

# Arithmetic Operators
# Addition
print(5 + 3)  # Output: 8

# Subtraction
print(10 - 4)  # Output: 6

# Multiplication
print(6 * 7)  # Output: 42

# Division
print(20 / 5)  # Output: 4.0

# Modulus (Remainder)
print(10 % 3)  # Output: 1

# Exponentiation
print(2 ** 3)  # Output: 8

# Floor Division
print(10 // 3)  # Output: 3

# Operator Precedence
result = 5 + 3 * 2  # Multiplication is performed before addition
print(result)  # Output: 11